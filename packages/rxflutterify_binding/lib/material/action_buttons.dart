final class CloseButtonIcon extends Wrapper {
  @Goto(_i1.CloseButtonIcon.new)
  CloseButtonIcon(
    $ $config, {
    _i2.Key? key,
  }) : super(
          Argument({#key: key}),
          builder: (args) => _i1.CloseButtonIcon(key: args(#key)),
        );
}

